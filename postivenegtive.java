class postivenegtive{
    static void Number(int num){
      
        if(num>0)
            System.out.println(num + " is POSITIVE NUMBER.");
        else 
            System.out.println(num + " is NEGATIVE NUMBER.");
       
    }
    public static void main( String args[] ) {
       
        int no1 = 20;
        int no2 = -100;
        

       
        Number(no1);
Number(no2);

        
    }
}